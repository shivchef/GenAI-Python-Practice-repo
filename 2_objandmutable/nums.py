#Integers

numberhai= 12
kitnahai = 3
puraKitna = numberhai +kitnahai
print(f"(add)pura number {puraKitna} itna hai")
puraKitna = numberhai -kitnahai
print(f"(substract)pura number {puraKitna} itna hai")


# two // means no decimal parts while dividing 
puraKitna = numberhai /kitnahai
print(f"(divide with decimal values)pura number {puraKitna} itna hai")
puraKitna = numberhai //kitnahai
print(f"(divide)pura number {puraKitna} itna hai")
puraKitna = numberhai %kitnahai
print(f"(remainder)pura number {puraKitna} itna hai")

